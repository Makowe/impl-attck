@logger.log_state
def add_round_key(state: np.ndarray, r_key: np.ndarray) -> np.ndarray:
    return state ^ r_key


@logger.log_state
def sub_bytes(state: np.ndarray) -> np.ndarray:
    return np.array([S_BOX[b] for b in state], dtype=np.uint8)


@logger.log_state
def shift_rows(state: np.ndarray) -> np.ndarray:
    res = np.zeros((16), dtype=np.uint8)
    # row 1
    res[0] = state[0]
    res[4] = state[4]
    res[8] = state[8]
    res[12] = state[12]

    # row 2
    res[1] = state[5]
    res[5] = state[9]
    res[9] = state[13]
    res[13] = state[1]

    # row 3
    res[2] = state[10]
    res[6] = state[14]
    res[10] = state[2]
    res[14] = state[6]

    # row 4
    res[3] = state[15]
    res[7] = state[3]
    res[11] = state[7]
    res[15] = state[11]
    return res


@logger.log_state
def mix_cols(state: np.ndarray) -> np.ndarray:
    cols = state.reshape(4, 4)
    return np.array([_mix_col(col) for col in cols]).reshape(16)


def expand_key(org_key: np.ndarray) -> np.ndarray:
    res = np.zeros((NUM_ROUNDS + 1, 16), dtype=np.uint8)
    res[0] = org_key

    for i in range(1, NUM_ROUNDS + 1):
        # set variable key to 16 byte buffer which is currently empty.
        key = res[i]

        # copy previous round key into current round key
        key[:] = res[i - 1]

        buf0 = S_BOX[key[13]]
        buf1 = S_BOX[key[14]]
        buf2 = S_BOX[key[15]]
        buf3 = S_BOX[key[12]]

        key[0] ^= buf0 ^ R_CON[i - 1]
        key[1] ^= buf1
        key[2] ^= buf2
        key[3] ^= buf3
        key[4] ^= key[0]
        key[5] ^= key[1]
        key[6] ^= key[2]
        key[7] ^= key[3]
        key[8] ^= key[4]
        key[9] ^= key[5]
        key[10] ^= key[6]
        key[11] ^= key[7]
        key[12] ^= key[8]
        key[13] ^= key[9]
        key[14] ^= key[10]
        key[15] ^= key[11]

    return res


def encrypt_block(plaintext: np.ndarray, key: np.ndarray) -> np.ndarray:
    expanded_key = expand_key(key)
    st = plaintext
    round = 0

    # 9 Rounds
    while round < NUM_ROUNDS - 1:
        st = add_round_key(st, expanded_key[round])
        st = sub_bytes(st)
        st = shift_rows(st)
        st = mix_cols(st)
        round += 1

    # 10th Round
    st = add_round_key(st, expanded_key[round])
    st = sub_bytes(st)
    st = shift_rows(st)

    # Final add round key
    st = add_round_key(st, expanded_key[round + 1])
    return st


